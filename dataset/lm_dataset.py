import json

from torch.utils.data import Dataset
import torch
import os

# First write the dataset class
os.environ["TOKENIZERS_PARALLELISM"] = "false"

class PretrainDataset(Dataset):
    def __init__(self, data_path, tokenizer, max_length=512):
        super().__init__()
        self.tokenizer = tokenizer
        self.max_length = max_length
        self.samples = self.load_data(data_path)

    # Implement the default method of dataset
    def load_data(self, path):
        samples = []
        with open(path, "r", encoding="utf-8") as f:
            for line_num, line in enumerate(f, 1):
                # 提取每一行内容放到sample
                data = json.loads(line.strip())
                samples.append(data)
        return samples

    # _len_
    def __len__(self):
        return len(self.samples)
    
    # __getitem__
    def __getitem__(self, index):
        sample = self.samples[index]
        # Encode with tokenizer
        # Truncation exceeding max_length, padding if less than max_length
        encoding = self.tokenizer(
            str(sample["text"]),
            max_length=self.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )

        input_ids = encoding.input_ids.squeeze()
        # Ignore the Y generated by padding
        loss_mask = input_ids != self.tokenizer.pad_token_id
        # The first to the penultimate token
        X = torch.tensor(input_ids[:-1], dtype=torch.long)
        # The second to the last token
        Y = torch.tensor(input_ids[1:], dtype=torch.long)
        loss_mask = torch.tensor(loss_mask[1:], dtype=torch.long)
        return X, Y, loss_mask